// increment operator
var i=10;
console.log(i);                       // 10
console.log(i++);                     // 10
// 'i++' first uses the current value in memory for calculation/operation, and then does increment.
console.log(i);                       // 11
console.log(++i);                     // 12
// '++i' first does increment, and then uses the new value for calulation/operation.
console.log(i);                       // 12

// decrement operator
var i=10;
console.log(i);                       // 10
console.log(i--);                     // 10
console.log(i);                       // 9
console.log(--i);                     // 8
console.log(i);                       // 8
